    private String validarCampos() {
        String errores = "";
        /*
        if (comandoPDF.getText().isEmpty()) {
            errores = errores + "- ¡No se definió la ruta del visor de PDF!.";
        }
        if (rutaDestinoPDF.getText().isEmpty()) {
            errores = errores + "- ¡No se definió la ruta de los archivos PDF!.";
        }
        */
        if (rutaDestinoPG.getText().isEmpty()) {
            errores = errores + "- ¡No se definió la ruta de mysqldump!.";
        }        
        
        return errores;
    }
    
    private Configuracion cargarDatos() {
        Configuracion configuracion = new Configuracion();
        
        configuracion.setIdConfiguracion(this.configuracion.getIdConfiguracion());
        configuracion.setRutaVisorPdf(comandoPDF.getText());
        configuracion.setRutaDestinoArchivosPDF(rutaDestinoPDF.getText());
        configuracion.setSoloGuadarArchivosPDF(soloGuardarArchivoPDF.isSelected());
        configuracion.setRutaProgramasPG(rutaDestinoPG.getText());
        return configuracion;
    }
    
