    private void grabarBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_grabarBTActionPerformed
        String errores = validarCampos();
        
        if (errores.isEmpty()) {
            try {
                sistemaDAO.gestionConfiguracionesDB(cargarDatos());
                JOptionPane.showMessageDialog(null, "¡Cambios guardados!", "¡Atención!",
                    JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                Logger.getLogger(FormConfiguraciones.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, errores, "¡Atención!",
                JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_grabarBTActionPerformed

